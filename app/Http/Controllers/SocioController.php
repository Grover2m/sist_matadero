     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //Validamos
        $request->validate([
            "nom_apell"=> "required"

        ]);
        //Guardar
        $soc = new Socio;
        $soc -> nom_apell = $request->nom_apell;
        $soc -> ci = $request->ci;
        $soc -> fono = $request->fono;
        //$soc -> estado = $request->estado;
        $soc->save();
      
        //Respuesta
        return redirect("/socio");
    }

